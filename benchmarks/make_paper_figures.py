def fig2_ihdp_pehe():
    df = pd.read_csv(RESULTS / "ihdp_benchmark_raw.csv")
    df = df.dropna(subset=["pehe"])

    rename = {
        "RX-Learner (robust)":            "Bayesian X-Learner\n(XGB-MSE)",
        "RX-Learner (robust+overlap)":    "Bayesian X-Learner\n(+ overlap)",
        "RX-Learner (CB-Huber δ=0.5)":    "Bayesian X-Learner\n(CB-Huber δ=0.5)",
        "RX-Learner (CB-Huber δ=1.345)":  "Bayesian X-Learner\n(CB-Huber δ=1.345)",
        "RX-Learner (std)":               "RX-Learner (std)",
        "S-Learner":                       "S-Learner",
        "T-Learner":                       "T-Learner",
        "X-Learner (std)":                 "X-Learner (std)",
        "EconML Forest":                   "EconML Forest",
    }
    df = df[df["estimator"].isin(rename)].copy()
    df["pretty"] = df["estimator"].map(rename)

    order = df.groupby("pretty")["pehe"].median().sort_values().index.tolist()

    fig, ax = plt.subplots(figsize=(7.0, 3.3))
    data = [df.loc[df["pretty"] == name, "pehe"].values for name in order]
    bp = ax.boxplot(data, tick_labels=order, patch_artist=True, widths=0.55,
                    medianprops={"color": "black"})
    for patch, name in zip(bp["boxes"], order):
        if "Bayesian X-Learner\n(XGB-MSE)" in name:
            patch.set_facecolor("steelblue")
            patch.set_alpha(0.85)
        elif "Bayesian X-Learner" in name:
            patch.set_facecolor("lightsteelblue")
            patch.set_alpha(0.7)
        else:
            patch.set_facecolor("lightgray")
            patch.set_alpha(0.7)

    ax.set_ylabel(r"$\sqrt{\varepsilon_{\mathrm{PEHE}}}$ (lower is better)")
    ax.set_xticklabels(order, rotation=25, ha="right")
    ax.grid(True, axis="y", alpha=0.25)
    ax.set_title("IHDP PEHE across 5 replications (CEVAE preprocessing)")
    fig.tight_layout()
    fig.savefig(FIG_DIR / "fig2_ihdp_pehe.pdf")
    plt.close(fig)
    print(f"wrote {FIG_DIR / 'fig2_ihdp_pehe.pdf'}")


# ------------------------- fig 3: Huber curves ---------------------------

def _huber_minimax_delta(eps):
    """Solve φ(δ)/δ − (1−Φ(δ)) = ε/(2(1−ε)) for δ."""
    if eps <= 0:
        return np.inf
    if eps >= 0.5:
        return 1e-6
    rhs = eps / (2 * (1 - eps))
    f = lambda d: norm.pdf(d) / d - (1 - norm.cdf(d)) - rhs
    return brentq(f, 1e-3, 50.0)


def _huber_are(delta):
    """ARE of Huber(δ) under Gaussian: E[ψ']^2 / E[ψ²]."""
    if np.isinf(delta):
        return 1.0
    # E[ψ_δ'] = P(|r| ≤ δ) = 2Φ(δ) − 1
    Epsi_prime = 2 * norm.cdf(delta) - 1
    # E[ψ_δ²] = ∫ r² φ(r) 1(|r|≤δ) dr + δ² · 2(1-Φ(δ))
    # inner integral via integration by parts: ∫_{-δ}^δ r² φ dr = 2Φ(δ)−1 − 2δφ(δ)
    inner = (2 * norm.cdf(delta) - 1) - 2 * delta * norm.pdf(delta)
    Epsi_sq = inner + delta ** 2 * 2 * (1 - norm.cdf(delta))
    return Epsi_prime ** 2 / Epsi_sq


def fig3_huber_curves():
    eps_grid = np.linspace(1e-4, 0.49, 200)
    delta_grid = np.array([_huber_minimax_delta(e) for e in eps_grid])

    d_for_are = np.linspace(0.1, 5.0, 200)
    are_grid = np.array([_huber_are(d) for d in d_for_are])

    presets = [
        ("mild\n(δ=1.345)",     0.05, 1.345),
        ("moderate\n(δ=1.0)",   0.10, 1.000),
        ("severe\n(δ=0.5)",     0.40, 0.500),
    ]

    fig, (ax_d, ax_a) = plt.subplots(1, 2, figsize=(7.0, 2.9))

    ax_d.plot(eps_grid * 100, delta_grid, "-", color="steelblue", linewidth=1.5)
    for label, eps, dval in presets:
        ax_d.plot(eps * 100, dval, "ro", markersize=6)
        ax_d.annotate(label, (eps * 100, dval),
                      textcoords="offset points", xytext=(8, 4), fontsize=7.5)
    ax_d.set_xlabel(r"assumed contamination rate $\epsilon$ (%)")
    ax_d.set_ylabel(r"minimax-optimal $\delta^\star(\epsilon)$")
    ax_d.set_title(r"(a) Huber (1964) minimax $\delta$")
    ax_d.set_ylim(0, 3.2)
    ax_d.grid(True, alpha=0.25)

    ax_a.plot(d_for_are, are_grid, "-", color="steelblue", linewidth=1.5)
    for label, eps, dval in presets:
        ax_a.plot(dval, _huber_are(dval), "ro", markersize=6)
        ax_a.annotate(f"δ={dval}\nARE={_huber_are(dval):.3f}", (dval, _huber_are(dval)),
                      textcoords="offset points", xytext=(8, -18), fontsize=7.5)
    ax_a.axhline(1.0, color="gray", linestyle="--", linewidth=0.8)
    ax_a.set_xlabel(r"Huber tuning $\delta$")
    ax_a.set_ylabel(r"ARE under $\mathcal{N}(0,1)$")
    ax_a.set_title("(b) asymptotic relative efficiency")
    ax_a.set_ylim(0.5, 1.05)
    ax_a.grid(True, alpha=0.25)

    fig.tight_layout()
    fig.savefig(FIG_DIR / "fig3_huber_curves.pdf")
    plt.close(fig)
    print(f"wrote {FIG_DIR / 'fig3_huber_curves.pdf'}")


# ------------------------- fig 5: tail-signal posteriors -----------------

def fig5_tail_posteriors():
    df = pd.read_csv(RESULTS / "tail_heterogeneous_cate_raw.csv")
    # Approximate posterior density for each (basis, config) cell via a
    # Gaussian centered at τ̂_whale with sigma = (ci_hi − ci_lo)/(2·1.96).
    fig, axes = plt.subplots(2, 3, figsize=(7.0, 4.5), sharex=True, sharey=True)

    bases = ["intercept", "tail_aware"]
    configs = ["Gaussian", "Welsch", "Welsch+EVT"]
    colors = {"Gaussian": "gray", "Welsch": "steelblue", "Welsch+EVT": "crimson"}

    grid = np.linspace(-5, 20, 500)
    for i, basis in enumerate(bases):
        for j, config in enumerate(configs):
            ax = axes[i, j]
            cell = df[(df["basis"] == basis) & (df["config"] == config)]
            for _, row in cell.iterrows():
                mu = row["tau_whale"]
                sigma = max(
                    (row["ci_whale_hi"] - row["ci_whale_lo"]) / (2 * 1.96),
                    0.02,
                )
                dens = norm.pdf(grid, loc=mu, scale=sigma)
                ax.plot(grid, dens, "-", color=colors[config], alpha=0.45, linewidth=1)
            ax.axvline(10.0, color="black", linestyle="--", linewidth=0.9, label=r"$\tau_{\text{whale}}=10$")
            ax.set_title(f"{basis} / {config}", fontsize=8.5)
            ax.set_xlim(-3, 18)
            ax.grid(True, alpha=0.2)
            if i == 1:
                ax.set_xlabel(r"$\hat\tau_{\text{whale}}$")
            if j == 0:
                ax.set_ylabel("posterior density (approx.)")

    axes[0, 0].legend(frameon=False, loc="upper right", fontsize=7)
    fig.suptitle(r"Posterior of $\hat\tau_{\text{whale}}$ by (basis, likelihood)"
                 r"  — dashed line = true $\tau_{\text{whale}} = 10$", fontsize=9.5)
    fig.tight_layout()
    fig.savefig(FIG_DIR / "fig5_tail_posteriors.pdf")
    plt.close(fig)
    print(f"wrote {FIG_DIR / 'fig5_tail_posteriors.pdf'}")


# ------------------------- fig 4: trace diagnostics ----------------------

def fig4_posterior_traces():
    """One representative fit's trace plot + R̂/ESS summary."""
    from sert_xlearner.targeted_bayesian_xlearner import TargetedBayesianXLearner
    from numpyro.diagnostics import effective_sample_size, gelman_rubin

    rng = np.random.default_rng(0)
    N, p = 500, 5
    X = rng.normal(0, 1, size=(N, p))
    true_tau = 2.5
    pi = 1 / (1 + np.exp(-0.3 * X[:, 0]))
    W = rng.binomial(1, np.clip(pi, 0.1, 0.9))
    Y0 = X[:, 0] + 0.5 * X[:, 1] + rng.normal(0, 1, size=N)
    Y = np.where(W == 1, Y0 + true_tau, Y0)

    n_chains, n_draws = 2, 800
    model = TargetedBayesianXLearner(
        num_warmup=400, num_samples=n_draws, num_chains=n_chains,
        random_state=0,
    )
    model.fit(X, Y, W)
    beta = np.asarray(model.mcmc_samples["beta"])  # (chains*draws, basis_dim) flat
    # Intercept-only default, take the first basis column
    flat = beta[..., 0] if beta.ndim == 2 else beta
    # Re-split into chains
    n_total = flat.shape[0]
    per = n_total // n_chains
    chains_beta = flat[: per * n_chains].reshape(n_chains, per)

    try:
        rhat = float(gelman_rubin(chains_beta))
        ess = float(effective_sample_size(chains_beta))
    except Exception:
        rhat, ess = float("nan"), float("nan")

    fig, (ax_t, ax_h) = plt.subplots(1, 2, figsize=(7.0, 2.7))
    for c, chain in enumerate(chains_beta):
        ax_t.plot(chain, linewidth=0.6, alpha=0.8, label=f"chain {c+1}")
    ax_t.axhline(true_tau, color="black", linestyle="--", linewidth=0.9,
                 label=rf"true $\tau = {true_tau}$")
    ax_t.set_xlabel("draw")
    ax_t.set_ylabel(r"$\beta$ (intercept = CATE)")
    ax_t.set_title(f"(a) trace  |  R̂ = {rhat:.3f},  ESS = {ess:.0f}")
    ax_t.legend(frameon=False, fontsize=7.5)
    ax_t.grid(True, alpha=0.25)

    ax_h.hist(chains_beta.ravel(), bins=40, color="steelblue",
              edgecolor="white", alpha=0.85)
    ax_h.axvline(true_tau, color="black", linestyle="--", linewidth=0.9)
    ax_h.set_xlabel(r"$\beta$")
    ax_h.set_ylabel("count")
    ax_h.set_title("(b) posterior marginal")
    ax_h.grid(True, alpha=0.25)

    fig.tight_layout()
    fig.savefig(FIG_DIR / "fig4_posterior_traces.pdf")
    plt.close(fig)
    print(f"wrote {FIG_DIR / 'fig4_posterior_traces.pdf'}")


