# ------------------------- fig 1: whale density --------------------------

def fig1_whale_density():
    xgb = pd.read_csv(RESULTS / "whale_density_raw.csv")
    cb  = pd.read_csv(RESULTS / "whale_density_raw_catboost_huber.csv")

    def _agg(df):
        g = df.groupby(["density", "variant"]).agg(
            rmse=("ate", lambda a: float(np.sqrt(np.mean(
                (np.asarray(a) - df.loc[a.index, "tau_true"].values) ** 2)))),
            coverage=("covered", "mean"),
        ).reset_index()
        return g

    xgb_robust = xgb[xgb["variant"].str.contains("robust", case=False)].copy()
    xgb_robust = xgb_robust.groupby("density").apply(
        lambda g: pd.Series({
            "rmse": float(np.sqrt(np.mean((g["ate"] - g["tau_true"]) ** 2))),
            "coverage": float(g["covered"].mean()),
        })
    ).reset_index()
    xgb_std = xgb[xgb["variant"].str.contains("std", case=False)].copy()
    xgb_std = xgb_std.groupby("density").apply(
        lambda g: pd.Series({
            "rmse": float(np.sqrt(np.mean((g["ate"] - g["tau_true"]) ** 2))),
            "coverage": float(g["covered"].mean()),
        })
    ).reset_index()
    cb_robust = cb[cb["variant"].str.contains("robust", case=False)].copy()
    cb_robust = cb_robust.groupby("density").apply(
        lambda g: pd.Series({
            "rmse": float(np.sqrt(np.mean((g["ate"] - g["tau_true"]) ** 2))),
            "coverage": float(g["covered"].mean()),
        })
    ).reset_index()

    fig, (ax_r, ax_c) = plt.subplots(1, 2, figsize=(7.0, 2.7))

    ax_r.plot(xgb_std["density"] * 100, xgb_std["rmse"], "-o",
              label="XGB-MSE (default)", color="crimson", markersize=4)
    ax_r.plot(cb_robust["density"] * 100, cb_robust["rmse"], "-s",
              label='CB-Huber (severity="severe")', color="steelblue", markersize=4)
    ax_r.set_xscale("log")
    ax_r.set_yscale("log")
    ax_r.set_xlabel("whale density (%)")
    ax_r.set_ylabel("RMSE of ATE")
    ax_r.set_title("(a) point accuracy")
    ax_r.legend(frameon=False, loc="best")
    ax_r.grid(True, which="both", alpha=0.25)

    ax_c.plot(xgb_std["density"] * 100, xgb_std["coverage"], "-o",
              label="XGB-MSE", color="crimson", markersize=4)
    ax_c.plot(cb_robust["density"] * 100, cb_robust["coverage"], "-s",
              label="CB-Huber", color="steelblue", markersize=4)
    ax_c.axhline(0.95, color="gray", linestyle="--", linewidth=0.8,
                 label="nominal 95%")
    ax_c.set_xscale("log")
    ax_c.set_xlabel("whale density (%)")
    ax_c.set_ylabel("coverage of true ATE")
    ax_c.set_title("(b) interval calibration")
    ax_c.set_ylim(-0.05, 1.1)
    ax_c.legend(frameon=False, loc="best")
    ax_c.grid(True, which="both", alpha=0.25)

    fig.tight_layout()
    fig.savefig(FIG_DIR / "fig1_whale_density.pdf")
    plt.close(fig)
    print(f"wrote {FIG_DIR / 'fig1_whale_density.pdf'}")


